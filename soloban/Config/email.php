/**
 * CahePHP メール設定定義.
 *
 * 各種設定は全てconfig/配下に設置
 */
class EmailConfig
{
    public $default = [];

    public $notifier = [];

    public function __construct()
    {
        $config = include APP.'Config'.DS.APP_ENV.DS.'email.php';

        $transports = ['default', 'notifier'];

        foreach ($transports AS $transport) {
            if (isset($config[$transport])) {
                $this->$transport = $config[$transport];
            }
        }

    }
}

