/**
 * CahePHP データベース設定定義.
 *
 * 各種設定は全てconfig/配下に設置
 */
class DATABASE_CONFIG
{
    public $default = [];
    public $session = [];
    public $s3 = [];

    public function __construct()
    {
        $database = include APP.'Config'.DS.APP_ENV.DS.'database.php';
        $this->default = $database['rdb'];

        $this->session = $database['rdb'];
        $this->storage = $database['s3'];
    }
}

